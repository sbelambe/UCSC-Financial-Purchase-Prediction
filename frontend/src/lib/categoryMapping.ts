/** Returns all lowercase original category strings that map to the given broad category value. */
export function getOriginalCategoriesForBroad(broadCategoryValue: string): string[] {
  if (broadCategoryValue === 'all') return [];
  return Object.entries(ORIGINAL_TO_BROAD)
    .filter(([, broad]) => broad === broadCategoryValue)
    .map(([orig]) => orig);
}

// For condensed groups, the representative row_values.Category is the alphabetically
// first category across all rows in the group — unreliable for filtering. Map the
// condensed_group name directly to the right broad category instead.
const CONDENSED_GROUP_TO_BROAD: Record<string, string> = {
  'gift cards': 'Gifts & Miscellaneous',
  'food bulk purchases': 'Food & Dining',
  'business services': 'Services & Subscriptions',
  'at&t bills': 'Services & Subscriptions',
  'order summaries': 'Services & Subscriptions',
};

/** Returns the broad category for an item, or null if not mapped. */
export function getBroadCategory(item: {
  condensed_group?: string | null;
  row_values?: Record<string, string | number | null>;
}): string | null {
  if (item.condensed_group) {
    const key = item.condensed_group.trim().toLowerCase();
    return CONDENSED_GROUP_TO_BROAD[key] ?? null;
  }
  const raw = item.row_values?.['Category'] ?? item.row_values?.['category'] ?? '';
  const normalized = String(raw).trim().toLowerCase();
  return ORIGINAL_TO_BROAD[normalized] ?? null;
}

/** Returns true if item belongs to the given broad category value. */
export function itemMatchesBroadCategory(
  item: { condensed_group?: string | null; row_values?: Record<string, string | number | null> },
  broadCategoryValue: string
): boolean {
  if (broadCategoryValue === 'all') return true;
  return getBroadCategory(item) === broadCategoryValue;
}
